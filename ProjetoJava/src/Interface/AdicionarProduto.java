    private void EnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnviarActionPerformed
        // TODO add your handling code here:
        int codigoProduto = Integer.valueOf(codigoEntrada.getText());
        int codigoFornecedor =Integer.valueOf(fornecedorEntrada.getText());
        int codigoCategoria =Integer.valueOf(categoriaEntrada.getText());
        Fornecedores fornecedor = this.controller.getFornecedorCodigo(codigoFornecedor);
        Categoria categoria = this.controller.getCategoriaCodigo(codigoCategoria);
        double preco = Double.valueOf(precoEntrada.getText());
        int qtd = Integer.valueOf(qtdEntrada.getText());
        int codigoUnidade = Integer.valueOf(unidadeEntrada.getText());
        Unidade unidade = controller.getUnidaedCodigo(codigoUnidade);
        
        Produto novoProduto = new Produto(categoria,codigoProduto,fornecedor,nomeEntrada.getText(),preco,qtd,unidade);
        
        controller.adicionarProduto(novoProduto);
    }//GEN-LAST:event_EnviarActionPerformed

